def count_all_lights():
    count = 0
    for row in grid:
        for light in row:
            if light == '#':
                 count+= 1
    return count
   
   
def process_light(x,y):
    """count the adjacent lights, and return the state of the light to be processed"""
    lights = 0
    # count neighbors 
    for x2 in [-1,0,1]:
        for y2 in [-1,0,1]:
            if not (x2, y2) == (0,0):
                neighbors_x = x + x2
                neighbors_y = y + y2
                if (neighbors_x >= 0 and neighbors_x <= 99) and (neighbors_y >= 0 and neighbors_y <= 99):
                    if prior_grid[neighbors_y][neighbors_x] == '#':
                        lights += 1
    if prior_grid[y][x] == '.':
        # A light which is off turns on if exactly 3 neighbors are on, and stays off otherwise.
        if lights == 3:
            grid[y][x] = '#'
    else:
        # A light which is on stays on when 2 or 3 neighbors are on, and turns off otherwise.
        if lights != 2 and lights != 3:
        	grid[y][x] = '.'
            
                	
